#!/usr/bin/env python3
"""
Module for a simple type-annotated addition function.
This module defines a function that takes two floats and returns their sum.
"""


def add(a: float, b: float) -> float:
    """
    Return the sum of two floating-point numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b

